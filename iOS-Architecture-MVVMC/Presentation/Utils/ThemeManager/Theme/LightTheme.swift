import UIKit

struct LightTheme: ThemeStyle {
    var titleColor: UIColor {
        return UIColor.black
    }

    var primaryColor: UIColor {
        return UIColor.lightGray
    }

    var secondaryColor: UIColor {
        return UIColor.white
    }
}
